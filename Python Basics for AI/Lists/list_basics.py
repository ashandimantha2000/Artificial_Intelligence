#defining the list
d=[6, "ashan", 90, 8, 3.4, ["new", 7]]

#printing the list
print(d)

#calling elements in the 2nd index
print(d[2])


#calling elements in the index 1:3 (Slicing)
#to get since 3, index 4 should be coded
print(d[1:4])

#calling the elements after index 2
print(d[2:])


#calling the elements before index 2
print(d[:2])